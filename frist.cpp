#include <iostream>
#include <string>
using namespace std;

int main() {
  // s using to give white space before the mini heartl
  string s = "               ";
  cout  << endl << endl << "\n\n\n\n";
  
  /* to show mini heart
      using both of space and * character */
  cout <<   s  <<  "     **     ** "       << endl ;
  cout <<   s  <<  "     *********  "  << endl ;
  cout <<   s  <<  "      *******  "    << endl ;
  cout <<   s  <<  "        *** "          << endl ;
  cout <<   s  <<  "         *  "           << endl ;
  
  return 0; 
}
